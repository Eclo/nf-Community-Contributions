        // I2C controller
        private static I2cDevice SSD1306;

        private const int Width = 128;
        private const int Height = 64;

        // Constructor for i2c bus
        public OLED(string device, byte DeviceAddress)
        {
            SSD1306 = I2cDevice.FromId(device, new I2cConnectionSettings(DeviceAddress));
        }

        /// <summary>
        /// Write bytes to device.
        /// </summary>
        /// <param name="buffer">Bytes to be write</param>
        /// <param name="isData">True if buffer is data, otherwise it is a command</param>
        private void WriteBytes(byte[] buffer, bool isData = false)
        {
            byte[] txBuffer = new byte[buffer.Length + 1];

            // if not data, first byte will be 0x0 (command address)
            if (isData)
            {
                // data address
                txBuffer[0] = 0x40;
            }

            Array.Copy(buffer, 0, txBuffer, 1, buffer.Length);

            var result = SSD1306.WritePartial(txBuffer);
        }

        /// <summary>
        /// Clear display.
        /// </summary>
        public void Clear()
        {
            byte[] tmpCmd = new byte[] { 0X0, 0x00, 0x10 };
            byte[] tmp = new byte[128];

            for (byte i = 0; i < 8; i++)
            {
                tmpCmd[0] = (byte)(0xb0 + i);
                WriteBytes(tmpCmd, false);
                WriteBytes(tmp, true);
            }

        }

        /// <summary>
        /// Light up display.
        /// </summary>
        public void DisplayOn()
        {
            byte[] tmp = new byte[] { 0X8D, 0X14, 0XAF };
            WriteBytes(tmp);
        }

        /// <summary>
        /// Light off display.
        /// </summary>
        public void DisplayOff()
        {
            byte[] tmp = new byte[] { 0X8D, 0X10, 0XAE };
            WriteBytes(tmp);
        }

        /// <summary>
        /// Set cursor position.
        /// </summary>
        /// <param name="column"></param>
        /// <param name="line"></param>
        private void SetPosition(byte column, byte line)
        {
            byte[] tmp = new byte[] { (byte)(0xb0 + line), (byte)(((column & 0xf0) >> 4) | 0x10), (byte)((column & 0x0f) | 0x01) };
            WriteBytes(tmp);
        }

        /// <summary>
        /// Show a character on display.
        /// </summary>
        /// <param name="x">Horizontal start position.</param>
        /// <param name="y">Vertical start position.</param>
        /// <param name="value">character to show on display.</param>
        public void ShowChar(byte x, byte y, byte value)
        {
            byte[] buffer = new byte[8];
            byte c = 0;
            c = (byte)(value - ' ');

            if (x > Width - 1)
            {
                x = 0;
                y = (byte)(y + 2);
            }

            SetPosition(x, y);

            Array.Copy(F8X16, (c * 16), buffer, 0, 8);
            WriteBytes(buffer, true);

            SetPosition(x, (byte)(y + 1));

            Array.Copy(F8X16, (c * 16 + 8), buffer, 0, 8);
            WriteBytes(buffer, true);
        }

        /// <summary>
        /// Show a string on display.
        /// </summary>
        /// <param name="x">Horizontal start position.</param>
        /// <param name="y">Vertical start position.</param>
        /// <param name="value">message to show on display.</param>
        /// <remarks>
        /// Display has 4 lines by 16 columns. Coordinates must be inside the display area.
        /// The first line (top one) shows in yellow color.
        /// Thre remaingin ones show in light blue.
        /// </remarks>
        public void ShowString(byte x, byte y, string value)
        {
            int j = 0;
            byte columnTmp = x, lineTmp = y;
            char[] buffer = value.ToCharArray();

            while ( j < buffer.Length && (buffer[j] != '\0') )
            {
                if (('\r' == buffer[j]) && ('\n' == buffer[j + 1]))
                {  
                    // this is a CR+LF sequence
                    while (columnTmp <= 120)
                    {  
                        // fill rest chars in current line
                        ShowChar(columnTmp, lineTmp, (byte)' ');
                        columnTmp += 8;
                    }

                    j += 2;
                }
                else if (('\r' == buffer[j]) || ('\n' == buffer[j]))
                {   
                    // CR or LF
                    while (columnTmp <= 120)
                    {  
                        // fill rest chars in current line
                        ShowChar(columnTmp, lineTmp, (byte)' ');
                        columnTmp += 8;
                    }

                    j += 1;
                }
                else
                {
                    if (columnTmp > 120)
                    {  
                        // overflows the last column, goto next line
                        columnTmp = 0;
                        lineTmp += 2;

                        if (lineTmp >= 8)
                        {  
                            // can only display 4 lines
                            break;
                        }
                    }

                    ShowChar(columnTmp, lineTmp, (byte)buffer[j]);

                    columnTmp += 8;

                    j++;
                }
            }
        }


        /// <summary>
        ///  Initialize SSD1306 registers
        ///</summary>
        public void Initialize()
        {
            WriteBytes(new byte[] { 0xAE }); // turn off oled panel
            WriteBytes(new byte[] { 0x00 }); // set low column address
            WriteBytes(new byte[] { 0x10 }); // set high column address
            WriteBytes(new byte[] { 0x40 }); // set start line address  Set Mapping RAM Display Start Line (0x00~0x3F)
            WriteBytes(new byte[] { 0x81 }); // set contrast control register
            WriteBytes(new byte[] { 0xCF }); // Set SEG Output Current Brightness
            WriteBytes(new byte[] { 0xA1 }); // Set SEG/Column Mapping
            WriteBytes(new byte[] { 0xC8 }); // Set COM/Row Scan Direction
            WriteBytes(new byte[] { 0xA6 }); // set normal display
            WriteBytes(new byte[] { 0xA8 }); // set multiplex ratio(1 to 64)
            WriteBytes(new byte[] { 0x3f }); // 1/64 duty
            WriteBytes(new byte[] { 0xD3 }); // set display offset	Shift Mapping RAM Counter (0x00~0x3F)
            WriteBytes(new byte[] { 0x00 }); // not offset
            WriteBytes(new byte[] { 0xd5 }); // set display clock divide ratio/oscillator frequency
            WriteBytes(new byte[] { 0x80 }); // set divide ratio, Set Clock as 100 Frames/Sec
            WriteBytes(new byte[] { 0xD9 }); // set pre-charge period
            WriteBytes(new byte[] { 0xF1 }); // Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
            WriteBytes(new byte[] { 0xDA }); // set com pins hardware configuration
            WriteBytes(new byte[] { 0x12 });
            WriteBytes(new byte[] { 0xDB }); // set vcomh
            WriteBytes(new byte[] { 0x40 }); // Set VCOM Deselect Level
            WriteBytes(new byte[] { 0x20 }); // Set Page Addressing Mode (0x00/0x01/0x02)
            WriteBytes(new byte[] { 0x02 }); //
            WriteBytes(new byte[] { 0x8D }); // set Charge Pump enable/disable
            WriteBytes(new byte[] { 0x14 }); // set(0x10) disable
            WriteBytes(new byte[] { 0xA4 }); // Disable Entire Display On (0xa4/0xa5)
            WriteBytes(new byte[] { 0xA6 }); // Disable Inverse Display On (0xa6/a7)   

            Clear();
            SetPosition(0, 0);
            WriteBytes(new byte[] { 0xAF }); // display ON
        }

